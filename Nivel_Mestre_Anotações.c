#include <stdio.h>
/*
Tipos de Operadores Relacionais
a > b (Maior que)
a < b (Menor que)
a >= b (maior ou igual a)
a <= b (Menor ou igual a)
a == b (igual a)
a != b (Diferente de)
*/
int main() {
    int a = 10;
    int b = 1;
    printf("a > b: %d\n", a > b);
    printf("a < b: %d\n", a < b);
    printf("a >= b: %d\n", a >= b);
    printf("a <= b: %d\n", a <= b);
    printf("a == b: %d\n", a == b);
    printf("a != b: %d\n", a != b);
    return 0;
} 
